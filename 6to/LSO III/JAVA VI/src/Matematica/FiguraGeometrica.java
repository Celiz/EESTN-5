package Matematica;

public class FiguraGeometrica {
	private int puntoX;
	private int puntoY;
	public int getPuntoX() {
		return puntoX;
	}
	public void setPuntoX(int puntoX) {
		this.puntoX = puntoX;
	}
	public int getPuntoY() {
		return puntoY;
	}
	public void setPuntoY(int puntoY) {
		this.puntoY = puntoY;
	}
	
	public void dibujar() {
		System.out.println("Dibujando figura geometrica");
	}
	

}
