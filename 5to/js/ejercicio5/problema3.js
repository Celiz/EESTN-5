let num1 = prompt("Introduza un numero");
let num2 = prompt("Introduza otro numero");
let num3 = prompt("Introduza otro numero");
let num4 = prompt("Introduza otro numero");

let suma = parseInt(num1) + parseInt(num2) + parseInt(num3) + parseInt(num4);
let producto = parseInt(num1) * parseInt(num2) * parseInt(num3) * parseInt(num4);

document.write('La suma es: ' + suma + '<br>' + 'El producto es: ' + producto);

